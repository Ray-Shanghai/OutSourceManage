package com.sd.farmework.pojo;

import com.sd.farmework.common.BaseInfo;
/**
 * @author yanxiaosan
 * 职位权限设置
 *
 */
public class PowerPositionInfo extends BaseInfo{

	private static final long serialVersionUID = -7220832587932029725L;
	
	private  String powerId;//权限ID
	private  String positionId;//职位
	private  String positionName;//职位名称
	private  String functionIds;//职位对应的操作功能
	
	
	public String getPositionName() {
		return positionName;
	}
	public void setPositionName(String positionName) {
		this.positionName = positionName;
	}
	public String getPowerId() {
		return powerId;
	}
	public void setPowerId(String powerId) {
		this.powerId = powerId;
	}
	public String getPositionId() {
		return positionId;
	}
	public void setPositionId(String positionId) {
		this.positionId = positionId;
	}
	public String getFunctionIds() {
		return functionIds;
	}
	public void setFunctionIds(String functionIds) {
		this.functionIds = functionIds;
	}
	@Override
	public String toString() {
		return "PowerPositionInfo [powerId=" + powerId + ", positionId="
				+ positionId + ", functionIds=" + functionIds + "]";
	}
	
	
}
