public class SdProductType extends BaseInfo{ 
	//产品类别主键 
	private int productTypeId; 
	//类别名称 
	private String productTypeName; 
	//类别序号 
	private String productTypeIndex; 
	// 
	private String sdId; 
    public SdProductType(){
	}
    public SdProductType(int productTypeId,String productTypeName,String productTypeIndex,String sdId){
		this.productTypeId=productTypeId;
		this.productTypeName=productTypeName;
		this.productTypeIndex=productTypeIndex;
		this.sdId=sdId;
	}
    public SdProductType(String productTypeName,String productTypeIndex,String sdId){
		this.productTypeName=productTypeName;
		this.productTypeIndex=productTypeIndex;
		this.sdId=sdId;
	}
	public void setproductTypeId(int productTypeId){ 
		this.productTypeId=productTypeId;
	}
	public int getproductTypeId(){ 
		return this.productTypeId;
	}
	public void setproductTypeName(String productTypeName){ 
		this.productTypeName=productTypeName;
	}
	public String getproductTypeName(){ 
		return this.productTypeName;
	}
	public void setproductTypeIndex(String productTypeIndex){ 
		this.productTypeIndex=productTypeIndex;
	}
	public String getproductTypeIndex(){ 
		return this.productTypeIndex;
	}
	public void setsdId(String sdId){ 
		this.sdId=sdId;
	}
	public String getsdId(){ 
		return this.sdId;
	}
	@Override
	public String toString() {
		return "SdProductType[productTypeId=" + productTypeId+",productTypeName=" + productTypeName+",productTypeIndex=" + productTypeIndex+",sdId=" + sdId+"]";
	}
}